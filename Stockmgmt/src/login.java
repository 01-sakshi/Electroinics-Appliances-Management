    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String myurl = "jdbc:mysql://localhost/stockmgmt";  //jdbc:subprotocol:subname 
        try
        {
        Connection myconn = DriverManager.getConnection(myurl,"root","");  //driver is activated,as no user is made so root is taken
        try
        {
        String q="select * from addemployee where IDNO = ? and password = ?";   //select query
        PreparedStatement myst = myconn.prepareStatement(q);
        myst.setString(1,jTextField1.getText());
        myst.setString(2,jPasswordField1.getText());
        ResultSet myres = myst.executeQuery();
        if(myres.next()==true)
        {
         this.hide();              //to hide login frame and open mainframe
         if(myres.getString("usertype").equalsIgnoreCase("admin"))
         {
          mainframe obj =  new mainframe();     //to open the individual frame after login frame
         obj.setVisible(true);   
         }
         else if(myres.getString("usertype").equalsIgnoreCase("admin "))
                 {        
         mainframe obj =  new mainframe();     //to open the individual frame after login frame
         obj.setVisible(true);
                 }
         else if(myres.getString("usertype").equalsIgnoreCase("manager"))
                 {        
         mangframe obj =  new mangframe();     //to open the individual frame after login frame
         obj.setVisible(true);
                 }
         else if(myres.getString("usertype").equalsIgnoreCase("employee"))
                 {        
         empframe obj =  new empframe();     //to open the individual frame after login frame
         obj.setVisible(true);
                 }
        }
        else
        {
         JOptionPane.showMessageDialog(rootPane,"Incorrect Username or Password");   
        }
        }
        catch(Exception e)
        {
         JOptionPane.showMessageDialog(rootPane,"Error Occured1 " + e.getMessage());   
        }
        finally
        {
         myconn.close();   //to close the connection  
        }
        }
        catch(Exception e)
                {
                JOptionPane.showMessageDialog(rootPane,"Error Occured2 " + e.getMessage());    
                }
    }//GEN-LAST:event_jButton1ActionPerformed

